
// Loops infinitos
// Un bucle infinito es un bucle que nunca termina su ejecución.
// Esto puede ocurrir si la condición del bucle nunca se vuelve falsa o si no hay una 
// instrucción que modifique la condición.
// Es importante evitar los bucles infinitos, ya que pueden bloquear la ejecución del programa.
//
// Ejemplo de bucle infinito con while
let contadorInfinito = 1;
while (contadorInfinito <= 5) {
    console.log("Este es un bucle infinito");
    // Aquí falta una instrucción para modificar la condición, por lo que nunca terminará
}