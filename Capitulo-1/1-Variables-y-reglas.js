// Las variables son contenedores para almacenar datos.
// Puedes pensar en ellas como etiquetas que apuntan a un valor en la memoria.
// Por ejemplo, si tienes una caja con una etiqueta que dice "edad", puedes poner un número 
// dentro de esa caja y luego referenciarlo por su etiqueta.
// En JavaScript, puedes declarar variables usando 'var', 'let' o 'const'.

// Las reglas para nombrar variables son las siguientes:
// 1. Deben comenzar con una letra, un guion bajo (_) o un signo de dólar ($).
// 2. Pueden contener letras, números, guiones bajos y signos de dólar.
// 3. No pueden comenzar con un número.
// 4. No pueden ser palabras reservadas del lenguaje (como 'if', 'for', 'while', etc.).
// 5. Son sensibles a mayúsculas y minúsculas (por ejemplo, 'edad' y 'Edad' son diferentes).

// Ejemplo de declaración de variables:
const nombre = "Juan Ernesto"; // Variable que almacena una cadena de texto
console.log("Nombre:", nombre); // Imprime el valor de la variable nombre