
// Arrays
// Los arrays (arreglos o matrices) son estructuras de datos que permiten almacenar múltiples valores en una sola variable.
// Son objetos especializados para trabajar con colecciones ordenadas de elementos.

// Características principales
// Ordenados: Los elementos tienen una posición indexada (comenzando desde 0)
// Dinámicos: Pueden crecer o reducirse automáticamente
// Flexibles: Pueden contener diferentes tipos de datos
// Iterables: Se pueden recorrer fácilmente

// Crear un array
let frutas = ["manzana", "banana", "naranja"];

// Usando constructor
let numeros = new Array(1, 2, 3, 4, 5);

// Array vacío
let vacio = [];

// Array con diferentes tipos
let mixto = ["texto", 42, true, null, {nombre: "Juan"}];