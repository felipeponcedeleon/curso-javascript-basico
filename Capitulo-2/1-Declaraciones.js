
// Declaraciones - Declarations
// Una declaración es una instrucción que el intérprete de JavaScript puede ejecutar.
// Las declaraciones pueden ser de varios tipos, como declaraciones de variables, funciones, clases, etc.
//
// Tipos comunes de declaraciones:
// 1- Let
// 2- Const
// 3- Function
// 4- async function
// 5- Class
// 6- export
// 7- import

//--------------------------------------------------------------


// Statement - Declaración
// Una declaración Statement es una instrucción completa que realiza una acción.
//
// Ejemplos de declaraciones:
// 1- IF
// 2- Else
// 3- Switch
// 4- For
// 5- While
// 6- Do...while
// 7- Try...catch
// 8- Break
// 9- Continue
// 10- Return

//--------------------------------------------------------------


// Expresiones - Expressions
// Una expresión es cualquier fragmento de código que produce un valor.
// Las expresiones pueden ser tan simples como un número o una cadena, o tan complejas como una función o una
// operación matemática.
//
// Ejemplos de expresiones:
// 1- Literales (números, cadenas, booleanos)
// 2- Operaciones matemáticas (suma, resta, multiplicación, división)
// 3- Llamadas a funciones
// 4- Operaciones lógicas (AND, OR, NOT)
// 5- Operaciones de comparación (igualdad, desigualdad, mayor que, menor que)
// 6- Expresiones de objeto y array
// 7- Expresiones ternarias
// 8- Asignaciones
// 9- Expresiones regulares
// 10- Expresiones de plantilla (template literals)

//--------------------------------------------------------------
