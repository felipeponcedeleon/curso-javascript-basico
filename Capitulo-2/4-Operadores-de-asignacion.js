
// Operadores de Asignación
// Los operadores de asignación se utilizan para asignar valores a variables.
// Existen dos tipos principales: asignación simple y asignación compuesta.
//
// Asignación simple
let x = 10; // Asigna el valor 10 a la variable x
let y = 20; // Asigna el valor 20 a la variable y

let z = x + y; // Asigna la suma de x e y a la variable z
console.log(z); // Imprime el valor de z, que es 30

let c = x * y; // Asigna el producto de x e y a la variable c
console.log(c); // Imprime el valor de c, que es 200

// Asignación compuesta 
let d = 15; // Asigna el valor 15 a la variable d

d += 5; // Suma 5 a d (d = d + 5)
console.log(d); // Imprime el valor de d, que ahora es 20

d -= 3; // Resta 3 a d (d = d - 3)
console.log(d); // Imprime el valor de d, que ahora es 17

d *= 2; // Multiplica d por 2 (d = d * 2)
console.log(d); // Imprime el valor de d, que ahora es 34

d /= 4; // Divide d por 4 (d = d / 4)
console.log(d); // Imprime el valor de d, que ahora es 8.5

d %= 3; // Asigna el residuo de d dividido por 3 (d = d % 3)
console.log(d); // Imprime el valor de d, que ahora es 2.5

d **= 2; // Eleva d al cuadrado (d = d ** 2)
console.log(d); // Imprime el valor de d, que ahora es 6.25


