
// Operdores comunes en JavaScript
// Los operadores son símbolos que realizan operaciones sobre uno o más operandos (valores o variables).
//
// Algunos operadores comunes en JavaScript son:
// 1- Aritméticos: +, -, *, /, %, ++, --
// 2- Asignación: =, +=, -=, *=, /=, %=
// 3- Comparación: ==, ===, !=, !==, >, <, >=, <=
// 4- Lógicos: &&, ||, !
// 5- Bit a bit: &, |, ^, ~, <<, >>, >>>
