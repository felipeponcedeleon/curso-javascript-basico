
// Condicional if-else

// La estructura if-else se utiliza para ejecutar un bloque de código si una condición es verdadera
// y otro bloque si la condición es falsa.
// Sintaxis:    
if (condicion) {
    // bloque de código a ejecutar si la condición es verdadera
} else {
    // bloque de código a ejecutar si la condición es falsa
}

// Ejemplo de uso de if-else para verificar si un número es positivo o negativo
let numero = -5;

if (numero >= 0) {
    console.log("El número es positivo");
} else {
    console.log("El número es negativo");
}
// En este ejemplo, si la variable numero es mayor o igual a 0, se imprime "El número es positivo",
// de lo contrario, se imprime "El número es negativo". 
// La estructura if-else permite manejar dos posibles caminos de ejecución basados en una condición.
