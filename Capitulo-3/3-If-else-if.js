
// If-else-if
// La estructura if-else-if se utiliza para manejar múltiples condiciones.  
// Sintaxis:
if (condicion1) {
    // bloque de código a ejecutar si la condición1 es verdadera
} else if (condicion2) {
    // bloque de código a ejecutar si la condición2 es verdadera
} else {
    // bloque de código a ejecutar si ninguna de las condiciones anteriores es verdadera
}   

// Ejemplo de uso de if-else-if para clasificar un número como positivo, negativo o cero
let numero2 = 0;

if (numero2 > 0) {
    console.log("El número es positivo");
} else if (numero2 < 0) {
    console.log("El número es negativo");
} else {
    console.log("El número es cero");
}